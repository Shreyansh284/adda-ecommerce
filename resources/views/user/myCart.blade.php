                let minusBtn = counter.querySelector(".input-counter__minus");
                let plusBtn = counter.querySelector(".input-counter__plus");
                let inputField = counter.querySelector(".input-counter__text");

                minusBtn.addEventListener("click", function() {
                    let currentValue = parseInt(inputField.value);
                    if (currentValue > parseInt(inputField.min)) {
                        inputField.value = currentValue - 1;
                    }
                });

                plusBtn.addEventListener("click", function() {
                    let currentValue = parseInt(inputField.value);
                    inputField.value = currentValue + 1;
                });
            });


            function updateCartQuantity(productId, quantity) {
                $.ajax({
                    url: "{{ route('cart.update') }}", // Define this route in web.php
                    type: "POST",
                    data: {
                        _token: "{{ csrf_token() }}",
                        id: productId,
                        quantity: quantity
                    },
                    success: function(response) {
                        if (response.success) {
                            location.reload(); // Refresh page to update total price
                        }
                    },
                    error: function(error) {
                        console.log(error);
                    }
                });
            }
